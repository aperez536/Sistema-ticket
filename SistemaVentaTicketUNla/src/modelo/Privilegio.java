package modelo;

public class Privilegio {
	
	private int codigoPrivilegio;
	private String resposabilidades;
	
	public Privilegio(int codigoPrivilegio, String resposabilidades) throws Exception{
		super();
		this.codigoPrivilegio = codigoPrivilegio;
		this.resposabilidades = resposabilidades;
	}
	public int getCodigoPrivilegio() {
		return codigoPrivilegio;
	}
	public void setCodigoPrivilegio(int codigoPrivilegio) {
		this.codigoPrivilegio = codigoPrivilegio;
	}
	public String getResposabilidades() {
		return resposabilidades;
	}
	public void setResposabilidades(String resposabilidades) {
		this.resposabilidades = resposabilidades;
	}
	@Override
	public String toString() {
		return "Privilegio [codigoPrivilegio=" + codigoPrivilegio + ", resposabilidades=" + resposabilidades + "]";
	}
	

}
